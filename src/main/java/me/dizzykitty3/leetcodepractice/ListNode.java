package me.dizzykitty3.leetcodepractice;

/**
 * Definition for singly-linked list.
 */
@SuppressWarnings("unused")
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
